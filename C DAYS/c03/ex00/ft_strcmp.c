int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	comp;

	i = 0;
	while (s1[i] != '\0' && s1[i] != '\0' && s1[i] == s2[i])
	{
		i++;
	}
	comp = s1[i] - s2[i];
	return (comp);
}
