#include <unistd.h>
int main (int argc, char **argv)
{
  if (argc == 2)
{
	int i = 0;
  char *str = argv[1];
  while (str[i] != '\0')
  {
   if ((str[i] >= 65 && str[i] <= 77) || (str[i] >= 97 && str[i] <= 109))  
        str[i] += 13;   
   else if((str[i] >= 66 && str[i] <= 90) || (str[i] >= 110 && str[i] <= 122))  
        str[i] -= 13;
   write (1, &str[i] , 1);
   i++;
  }
}
}
